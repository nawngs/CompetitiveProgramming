ll n, a[100005], k;

bool check(ll x) {
	ll res = 0;
	for (int i = 1; i <= n; i++) res += a[i] / x;
	return res >= k;
}

void sol() {
	cin >> n >> k;
	for (int i = 1; i <= n; i++) cin >> a[i];
	ll l = 1, r = 1e9, mid, ans = 0;
	while (l <= r) {
		mid = (l + r) / 2;
		if (check(mid)) {
			ans = mid;
			l = mid + 1;
		}
		else r = mid - 1;
	}
	cout << ans;
}

